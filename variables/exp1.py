# COMPARISION OPERATOR IN PYTHON`
a,b=int(input('give value: of a:')),int(input('give value: of b:'))
# a=a+b

def p1():
 a+=b
 print('a+=b',a)
# a=a-b

def p2():
 a-=b
 print('a-=b',a)
# a=a*b

def p3():
 a*=b
 print('a*=b',a)
# a=a%b

def p4():
 a%=b
 print('a%=b',a)
# a=a**b

def p5():
 a**=b
 print('a**=b',a)


print(p1(a))
print(p2(a))
print(p3(a))
print(p4(a))
print(p5(a))